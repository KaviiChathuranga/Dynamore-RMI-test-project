    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       
        
        int x=jTable1.getSelectedRow();
        String oid=""+jTable1.getValueAt(x,0);
        try {
            OrdersDto ordersDto=CallCenterControler.searchOrder(oid);
            
            if(!this.oid.equalsIgnoreCase(oid)){
                CallCenterControler.release(this.oid);
                this.oid=oid;
            }
            if (ordersDto != null) {
                boolean isReserved = CallCenterControler.reserve(oid);
                if (isReserved) {
                   ItemDto itemDto=ItemControler.serarchItm(ordersDto.getItemCode());
                custNameTxt.setText(ordersDto.getName());
                custAddressTxt.setText(ordersDto.getAddress());
                custTelTxt.setText(ordersDto.getTel());
                qtyTxt.setText(""+ordersDto.getQty()); 
                itemNameTxt.setText(itemDto.getItemName());
                dateTxt.setText(ordersDto.getDate());
                typeLbl.setText("          "+ordersDto.getType());
                chef.setText(ordersDto.getChefName());
            
                } else {
                    JOptionPane.showMessageDialog(null,"Reseved");
                custNameTxt.setText("");
                custAddressTxt.setText("");
                custTelTxt.setText("");
                qtyTxt.setText(""); 
                itemNameTxt.setText("");
                dateTxt.setText("");
                typeLbl.setText("");
                chef.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(this, "No Customer found");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(Report.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
     int x=jTable1.getSelectedRow();
        String oid=""+jTable1.getValueAt(x,0);
        int qty=Integer.parseInt(qtyTxt.getText());
        String item=""+jTable1.getValueAt(x,3);
        
        String cust=""+jTable1.getValueAt(x,1);
        OrdersDto dto=null;
        ItemDto itemDto=null;
        try {
           itemDto=ItemControler.serarchItm(item);
           dto =CallCenterControler.searchOrder(oid);
        } catch (Exception ex) {
            Logger.getLogger(Report.class.getName()).log(Level.SEVERE, null, ex);
        }
        OrdersDto ordersDto=new OrdersDto(dto.getOid(),dateTxt.getText(),qty,itemDto.getItemCode(),cust,custNameTxt.getText(),custAddressTxt.getText(),custTelTxt.getText(),dto.getType(),dto.getStartTime(),dto.getEndTime(),dto.getChefName(),dto.getCount());
        try {
            boolean b= CallCenterControler.updateOrder(ordersDto);
            if (b) {
                JOptionPane.showMessageDialog(null,"The order is updated");
            }else{
                JOptionPane.showMessageDialog(null,"Sorry ! Order Update Is Failed !","",JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(Report.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1MouseClicked

