    private void load() {
              DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
                 dtm.setRowCount(0);
      
        try {
            List<OrdersDto>list=CallCenterControler.searchDate(date); 
            
            if(list==null){
                JOptionPane.showMessageDialog(null,"Empty Orders at Today !");
            }else{
                      
            for (OrdersDto ordersDto : list) {
                count++;
                Object []row={ordersDto.getOid(),ordersDto.getCid(),ordersDto.getQty(),ordersDto.getItemCode(),ordersDto.getType(),ordersDto.getCount()};
                dtm.addRow(row);
            }
            jLabel8.setText(""+count);
          }
        } catch (Exception ex) {
            Logger.getLogger(Report.class.getName()).log(Level.SEVERE, null, ex);
        }
        

    }
